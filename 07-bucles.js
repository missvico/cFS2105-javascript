for(let i = 0; i<3; i++){
    console.log(i)
}

//Resultado:
//0
//1
//2

//Explicacion:

//1) i vale 0, la condicion i<3 es VERDADERA, por lo que hace console.log(0)
//2) i++ --> ahora i vale 1
//3) i vale 1, la condicion i<3 es VERDADERA, por lo que hace console.log(1)
//4) i++ --> ahora i vale 2
//5) i vale 2, la condicion i<3 es VERDADERA, por lo que hace console.log(2)
//6) i++ --> ahora i vale 3
//7) i vale 3, la condicion i<3 es FALSE, por lo que es el FIN DEL LOOP.

